    def test_evaluate_all_possible_moves_on_empty_field_with_square(self):
        result = evaluate_all_possible_moves(Field(), Stone(10))
        self.assertEqual(
            Evaluation(
                height=2,
                lines_cleared=0,
                holes=0,
                bumpiness=2,
                bumps=1,
                bump_ratio=0.2222222222222222
            ),
            result[0]
        )

        for i in range(1, 8):
            self.assertEqual(
                Evaluation(
                    height=2,
                    lines_cleared=0,
                    holes=0,
                    bumpiness=4,
                    bumps=2,
                    bump_ratio=0.5
                ),
                result[i]
            )

        self.assertEqual(
            Evaluation(
                height=2,
                lines_cleared=0,
                holes=0,
                bumpiness=2,
                bumps=1,
                bump_ratio=0.2222222222222222
            ),
            result[8]
        )


